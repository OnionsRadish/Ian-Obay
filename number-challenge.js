function funcadd(a, b) {
    return a + b;
  }
function funcsub(a, b) {
    return a - b;
  }
function funcmulti(a, b) {
    return a * b;
  }
function funcdiv(a, b) {
    return a / b;
  }
console.log(funcadd(5, 5));
console.log(funcsub(5, 5));
console.log(funcmulti(5, 5));
console.log(funcdiv(5, 5));